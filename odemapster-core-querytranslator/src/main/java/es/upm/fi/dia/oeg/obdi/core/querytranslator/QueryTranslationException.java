public class QueryTranslationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public QueryTranslationException(String string) {
		super(string);
	}

	public QueryTranslationException(Throwable arg0) {
		super(arg0);
	}

	public QueryTranslationException(String message, Throwable cause) {
		super(message, cause);
	}

	
}
